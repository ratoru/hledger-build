func TestWriteImportStubs_Idempotent(t *testing.T) {
	dir := t.TempDir()
	cfg := &config.Config{
		ProjectRoot: dir,
		FirstYear:   2026,
		CurrentYear: 2026,
		Directories: config.Directories{Sources: "sources"},
	}
	steps := []runner.Step{
		{Output: "sources/bank/journal/2026/txns.journal", IsIngestJournal: true},
	}

	// Write once.
	if err := WriteImportStubs(cfg, steps); err != nil {
		t.Fatal(err)
	}
	path := filepath.Join(dir, "sources", "2026-imports.journal")
	info1, err := os.Stat(path)
	if err != nil {
		t.Fatal(err)
	}

	// Write again — mtime should not change (content unchanged).
	if err := WriteImportStubs(cfg, steps); err != nil {
		t.Fatal(err)
	}
	info2, err := os.Stat(path)
	if err != nil {
		t.Fatal(err)
	}
	if info1.ModTime() != info2.ModTime() {
		t.Error("stub was rewritten despite unchanged content")
	}
}
